#Int para Float: 
preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

#De float para Int
preco = 10.30
print(preco)
preco = int(preco)
print(preco)

preco = 10
print(preco)
print(preco // 2)

